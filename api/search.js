console.log('üîç API Search wird geladen...');

module.exports = async (req, res) => {
    console.log('üì° API Search Request:', req.method, req.query);
    
    // CORS headers
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'GET,OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'X-CSRF-Token, X-Requested-With, Accept, Accept-Version, Content-Length, Content-MD5, Content-Type, Date, X-Api-Version');

    if (req.method === 'OPTIONS') {
        console.log('‚úÖ CORS Preflight');
        return res.status(200).end();
    }

    if (req.method === 'GET') {
        const { q } = req.query;
        console.log('üîç Search Query:', q);

        if (!q || q.length < 2) {
            console.log('‚ùå Query zu kurz');
            return res.status(200).json([]);
        }

        try {
            // EINFACHE TEST DATEN
            const testPlayers = [
                {
                    id: '1',
                    name: 'Builderman',
                    displayName: 'Builderman',
                    avatar: '/api/placeholder-avatar',
                    hasVerifiedBadge: true
                },
                {
                    id: '2',
                    name: 'JohnDoe',
                    displayName: 'John',
                    avatar: '/api/placeholder-avatar',
                    hasVerifiedBadge: false
                },
                {
                    id: '3',
                    name: 'TestUser',
                    displayName: 'Test',
                    avatar: '/api/placeholder-avatar',
                    hasVerifiedBadge: false
                }
            ].filter(player => 
                player.name.toLowerCase().includes(q.toLowerCase())
            );

            console.log('‚úÖ Sende Ergebnisse:', testPlayers);
            return res.status(200).json(testPlayers);
            
        } catch (error) {
            console.error('‚ùå Search error:', error);
            return res.status(200).json([]);
        }
    }

    console.log('‚ùå Method not allowed:', req.method);
    return res.status(405).json({ error: 'Method not allowed' });
};